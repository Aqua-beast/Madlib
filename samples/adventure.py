def madlib():
    print("Let's go on an adventure!")

    # Input from the user
    adjective = input("Give me an adjective: ")
    noun = input("Choose a noun: ")
    verb = input("Pick a verb: ")
    adverb = input("Now an adverb: ")

    # Output the story
    print(f"\nOnce upon a time, in a {adjective} land, there was a brave {noun}.")
    print(f"This {noun} loved to {verb} {adverb} through the enchanted forest.")
    print(f"One day, the {noun} discovered a magical {adjective} {noun} and decided to {verb} {adverb} to explore its mysteries.")
    print(f"The {noun}'s {verb}ing adventures led to {adverb} amusing encounters and a {adjective} ending to the story.")
